class UIView {
    var backroundColor = ""
    var cornerRadius = 0
    var width = 0
    var height = 0
    
    init(backroundColor: String, cornerRadius: Int, width: Int, height: Int) {
        self.backroundColor = backroundColor
        self.cornerRadius = cornerRadius
        self.width = width
        self.height = height
    }
}
